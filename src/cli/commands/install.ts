import fs from 'fs'
import path from 'path'
import os from 'os'
import * as p from '@clack/prompts'
import type { Command } from 'commander'
import { Mailer } from '../../mailer'
import type { MailerConfig, TemplateLanguage } from '../../types'

const NPX_COMMAND = 'npx -y claude-code-mailer send --stdin'

const HOOK_EVENT_TYPES = ['Notification', 'Stop', 'SubagentStop'] as const

interface HookEntry {
  type: string
  command: string
}

interface HookGroup {
  hooks: HookEntry[]
}

interface ClaudeSettings {
  hooks?: Record<string, HookGroup[]>
  [key: string]: unknown
}

function getClaudeSettingsPath(): string {
  return path.join(os.homedir(), '.claude', 'settings.json')
}

function getGlobalConfigPath(): string {
  return path.join(os.homedir(), '.claude-code-mailer', '.env')
}

function readClaudeSettings(): ClaudeSettings {
  const settingsPath = getClaudeSettingsPath()

  if (!fs.existsSync(settingsPath)) {
    const claudeDir = path.dirname(settingsPath)
    if (!fs.existsSync(claudeDir)) {
      fs.mkdirSync(claudeDir, { recursive: true })
    }
    const defaultSettings: ClaudeSettings = { hooks: {} }
    fs.writeFileSync(settingsPath, JSON.stringify(defaultSettings, null, 2))
    return defaultSettings
  }

  const data = fs.readFileSync(settingsPath, 'utf8')
  return JSON.parse(data) as ClaudeSettings
}

function writeClaudeSettings(settings: ClaudeSettings): void {
  fs.writeFileSync(getClaudeSettingsPath(), JSON.stringify(settings, null, 2))
}

function isMailerHook(command: string): boolean {
  return command.includes('claude-code-mailer send --stdin') ||
         command.includes('claude-code-mailer/bin/cli.js send --stdin')
}

function hasMailerHook(settings: ClaudeSettings, eventType: string): boolean {
  const groups = settings.hooks?.[eventType]
  if (!groups) return false

  for (const group of groups) {
    if (group.hooks?.some(h => h.type === 'command' && isMailerHook(h.command))) {
      return true
    }
  }
  return false
}

function installHooks(): { installed: number; skipped: number } {
  const settings = readClaudeSettings()
  if (!settings.hooks) settings.hooks = {}

  let installed = 0
  let skipped = 0

  for (const eventType of HOOK_EVENT_TYPES) {
    if (hasMailerHook(settings, eventType)) {
      skipped++
      continue
    }
    if (!settings.hooks[eventType]) settings.hooks[eventType] = []
    settings.hooks[eventType].push({ hooks: [{ type: 'command', command: NPX_COMMAND }] })
    installed++
  }

  if (installed > 0) writeClaudeSettings(settings)
  return { installed, skipped }
}

function saveConfig(config: {
  smtpHost: string
  smtpPort: number
  smtpSecure: boolean
  smtpUser: string
  smtpPass: string
  fromEmail: string
  toEmail: string
  language: string
}): void {
  const configPath = getGlobalConfigPath()
  const configDir = path.dirname(configPath)
  if (!fs.existsSync(configDir)) fs.mkdirSync(configDir, { recursive: true })

  const content = `# Claude Code Mailer Configuration
# Generated by: npx claude-code-mailer install

SMTP_HOST=${config.smtpHost}
SMTP_PORT=${config.smtpPort}
SMTP_SECURE=${config.smtpSecure}
SMTP_USER=${config.smtpUser}
SMTP_PASS=${config.smtpPass}

FROM_EMAIL=${config.fromEmail}
TO_EMAIL=${config.toEmail}
SUBJECT_PREFIX=[Claude Code]

TEMPLATE_LANGUAGE=${config.language}

RETRY_ATTEMPTS=3
RETRY_DELAY=1000
TIMEOUT=10000
TLS_REJECT_UNAUTHORIZED=true
`
  fs.writeFileSync(configPath, content)
}

async function runSetupWizard(): Promise<void> {
  p.intro('Claude Code Mailer — Setup Wizard')

  const hasExistingConfig = fs.existsSync(getGlobalConfigPath())
  if (hasExistingConfig) {
    const overwrite = await p.confirm({
      message: `Config already exists at ${getGlobalConfigPath()}. Reconfigure?`,
      initialValue: false,
    })
    if (p.isCancel(overwrite) || !overwrite) {
      p.cancel('Skipped configuration, installing hooks only.')
      const { installed, skipped } = installHooks()
      p.outro(`Hooks: ${installed} installed, ${skipped} already present.`)
      return
    }
  }

  p.log.step('Step 1/3  SMTP Server')

  const smtpPreset = await p.select({
    message: 'Choose your email provider',
    options: [
      { value: 'gmail',  label: 'Gmail',              hint: 'smtp.gmail.com:587' },
      { value: '163',    label: '163 Mail',            hint: 'smtp.163.com:25' },
      { value: 'qq',     label: 'QQ Mail',             hint: 'smtp.qq.com:587' },
      { value: 'custom', label: 'Custom SMTP server',  hint: 'enter manually' },
    ],
  })
  if (p.isCancel(smtpPreset)) { p.cancel('Cancelled.'); process.exit(0) }

  const presets: Record<string, { host: string; port: number; secure: boolean }> = {
    gmail: { host: 'smtp.gmail.com',   port: 587, secure: false },
    '163': { host: 'smtp.163.com',     port: 25,  secure: false },
    qq:    { host: 'smtp.qq.com',      port: 587, secure: false },
    custom: { host: '', port: 587, secure: false },
  }
  const preset = presets[smtpPreset as string]

  const smtpHost = smtpPreset === 'custom'
    ? await p.text({ message: 'SMTP host', placeholder: 'smtp.example.com', validate: v => !v || v.length === 0 ? 'Required' : undefined })
    : preset.host
  if (p.isCancel(smtpHost)) { p.cancel('Cancelled.'); process.exit(0) }

  const smtpPortRaw = await p.text({
    message: 'SMTP port',
    initialValue: String(preset.port),
    validate: v => isNaN(Number(v)) || Number(v) < 1 ? 'Must be a valid port number' : undefined,
  })
  if (p.isCancel(smtpPortRaw)) { p.cancel('Cancelled.'); process.exit(0) }
  const smtpPort = Number(smtpPortRaw)

  const smtpSecureRaw = await p.select({
    message: 'Connection security',
    options: [
      { value: 'false', label: 'STARTTLS (port 587/25)', hint: 'recommended' },
      { value: 'true',  label: 'SSL/TLS (port 465/994)' },
    ],
    initialValue: preset.secure ? 'true' : 'false',
  })
  if (p.isCancel(smtpSecureRaw)) { p.cancel('Cancelled.'); process.exit(0) }
  const smtpSecure = smtpSecureRaw === 'true'

  p.log.step('Step 2/3  Account & Recipients')

  const smtpUser = await p.text({
    message: 'SMTP username (usually your email address)',
    placeholder: 'you@example.com',
    validate: v => !v ? 'Required' : undefined,
  })
  if (p.isCancel(smtpUser)) { p.cancel('Cancelled.'); process.exit(0) }

  const smtpPass = await p.password({
    message: 'SMTP password / app password',
    validate: v => !v ? 'Required' : undefined,
  })
  if (p.isCancel(smtpPass)) { p.cancel('Cancelled.'); process.exit(0) }

  const fromEmail = await p.text({
    message: 'From address',
    initialValue: smtpUser as string,
    validate: v => !v ? 'Required' : undefined,
  })
  if (p.isCancel(fromEmail)) { p.cancel('Cancelled.'); process.exit(0) }

  const toEmail = await p.text({
    message: 'Deliver notifications to (your inbox)',
    placeholder: 'you@example.com',
    initialValue: smtpUser as string,
    validate: v => !v ? 'Required' : undefined,
  })
  if (p.isCancel(toEmail)) { p.cancel('Cancelled.'); process.exit(0) }

  const language = await p.select({
    message: 'Email template language',
    options: [
      { value: 'zh-CN', label: 'Simplified Chinese  简体中文', hint: 'default' },
      { value: 'zh-HK', label: 'Traditional Chinese 繁體中文' },
      { value: 'en',    label: 'English' },
    ],
  })
  if (p.isCancel(language)) { p.cancel('Cancelled.'); process.exit(0) }

  p.log.step('Step 3/3  Test & Save')

  const s = p.spinner()
  s.start('Connecting to SMTP server and sending test email…')

  let testPassed = false
  try {
    const testConfig: MailerConfig = {
      smtp: {
        host: smtpHost as string,
        port: smtpPort,
        secure: smtpSecure,
        auth: { user: smtpUser as string, pass: smtpPass as string },
        rejectUnauthorized: true,
      },
      from: fromEmail as string,
      to: toEmail as string,
      subjectPrefix: '[Claude Code]',
      language: language as TemplateLanguage,
      retryAttempts: 1,
      retryDelay: 1000,
      timeout: 15000,
    }

    const mailer = new Mailer(testConfig)
    const result = await mailer.sendNotification('Stop', { sessionId: 'setup-test' }, {
      message: 'Setup test — if you received this email, Claude Code Mailer is working!',
    })

    if (result.success) {
      s.stop('Test email sent successfully.')
      testPassed = true
    } else {
      s.stop('Send failed.')
    }
  } catch (err) {
    s.stop(`Connection failed: ${(err as Error).message}`)
  }

  if (!testPassed) {
    const retry = await p.confirm({ message: 'Save config anyway and continue?', initialValue: false })
    if (p.isCancel(retry) || !retry) {
      p.cancel('Setup cancelled. Please check your SMTP settings and try again.')
      process.exit(1)
    }
  } else {
    const confirmed = await p.confirm({
      message: `Did you receive the test email at ${toEmail}?`,
      initialValue: true,
    })
    if (p.isCancel(confirmed)) { p.cancel('Cancelled.'); process.exit(0) }
    if (!confirmed) {
      p.log.warn('Check your spam folder. Config saved but hooks will be installed — you can re-run anytime.')
    }
  }

  saveConfig({
    smtpHost: smtpHost as string,
    smtpPort,
    smtpSecure,
    smtpUser: smtpUser as string,
    smtpPass: smtpPass as string,
    fromEmail: fromEmail as string,
    toEmail: toEmail as string,
    language: language as string,
  })
  p.log.success(`Config saved to ${getGlobalConfigPath()}`)

  const { installed, skipped } = installHooks()
  if (installed > 0) {
    p.log.success(`Hooks installed: ${HOOK_EVENT_TYPES.slice(0, installed).join(', ')}`)
  }
  if (skipped > 0) {
    p.log.info(`${skipped} hook(s) already present, skipped.`)
  }

  p.outro("You're all set! Claude Code will now email you when it needs attention.")
}

export function registerInstallCommand(program: Command): void {
  program
    .command('install')
    .description('Interactive setup wizard: configure SMTP, test, and install hooks')
    .action(async () => {
      await runSetupWizard()
    })

  program
    .command('uninstall')
    .description('Uninstall Claude Code hooks')
    .action(() => {
      const settings = readClaudeSettings()
      if (!settings.hooks) {
        console.log('No hooks found.')
        return
      }

      let removed = 0
      for (const eventType of Object.keys(settings.hooks)) {
        const before = settings.hooks[eventType].length
        settings.hooks[eventType] = settings.hooks[eventType].filter(
          group => !group.hooks?.some(h => h.type === 'command' && isMailerHook(h.command))
        )
        if (settings.hooks[eventType].length < before) removed++
        if (settings.hooks[eventType].length === 0) delete settings.hooks[eventType]
      }

      if (removed > 0) {
        writeClaudeSettings(settings)
        console.log(`Removed ${removed} hook(s).`)
      } else {
        console.log('No Claude Code Mailer hooks found.')
      }
    })
}
